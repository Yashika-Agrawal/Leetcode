class Solution{
  public:
    // Should return head of the modified linked list
    Node *sortedInsert(struct Node* head, int data) {
       if (head==NULL) return NULL;
       Node* node=new Node(data);
       Node* temp=head;
       if(data<=head->data)
       {
           node->next=head;
           return node;
       }
       while(temp->next!=NULL && temp->data<data)
       {
           if(temp->next->data>=data)
           {
               node->next=temp->next;
               temp->next=node;
               return head;
           }
           temp=temp->next;
       }
       temp->next=node;
       return head;
    }
};


